public class PrintFloatNumbers {
    public static void main(String[] args) {
        // Declare and initialize two float numbers
        float floatNumber1 = 3.14f;
        float floatNumber2 = 2.718f;

        // Print the float numbers
        System.out.println("Float Number 1: " + floatNumber1);
        System.out.println("Float Number 2: " + floatNumber2);
    }
}

